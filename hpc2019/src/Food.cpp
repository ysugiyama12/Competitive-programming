#pragma once
#include "Food.hpp"

//------------------------------------------------------------------------------
namespace hpc {

//------------------------------------------------------------------------------
Food::Food()
: mPosition()
, mHeight()
, mIsEaten()
{
}

//------------------------------------------------------------------------------
Food::Food(Point aPos, int aHeight)
: mPosition(aPos)
, mHeight(aHeight)
, mIsEaten()
{
}

//------------------------------------------------------------------------------
const Point& Food::pos() const
{
    return mPosition;
}

//------------------------------------------------------------------------------
int Food::height() const
{
    return mHeight;
}

//------------------------------------------------------------------------------
bool Food::isEaten() const
{
    return mIsEaten;
}

//------------------------------------------------------------------------------
void Food::setIsEaten(bool isEaten)
{
    mIsEaten = isEaten;
}

} // namespace
